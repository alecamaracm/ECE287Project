                    }
                    else
                    {
                        collideablesx.x = ((int)picturex.Layout.Left.To(LengthUnit.Point)).ToString();
                        collideablesx.y = ((int)picturex.Layout.Top.To(LengthUnit.Point)).ToString();
                    }

