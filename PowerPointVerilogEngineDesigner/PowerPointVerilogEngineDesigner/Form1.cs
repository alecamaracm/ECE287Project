                writer.WriteLine("\tif("+s+"Dir==1)");
                writer.WriteLine("\tbegin");
                if(s.EndsWith("X"))
                {
                    writer.WriteLine("\t\tif (" + s + " >= "+properties.maxX+" - " + properties.width + ")");
