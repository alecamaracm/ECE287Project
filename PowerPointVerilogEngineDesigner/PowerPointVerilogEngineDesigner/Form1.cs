                    {
                        collideables.Add(properties["COLLIDING"], new List<Collideables>());
                    }

                    if (collideables.ContainsKey(properties["COLLIDING"]) == false)
                    {
                        collideables.Add(properties["COLLIDING"], new List<Collideables>());
                    }

