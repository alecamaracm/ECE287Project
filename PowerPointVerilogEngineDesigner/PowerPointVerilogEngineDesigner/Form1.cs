                    {
                        int sp = properties.ContainsKey("AnimationSpeed") ? int.Parse(properties["AnimationSpeed"]) : 1;
                        bouncings.Add(properties["NAME"].Replace(" ", "_") + "X");
                        bouncings.Add(properties["NAME"].Replace(" ", "_") + "Y");
